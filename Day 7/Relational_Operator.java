

public class Relational_Operator {

	public static void main(String[] args)
	{
		int a =10;
		int b =20;
		
		boolean c = a>b;
		System.out.println(c);
		
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}
	
}
